// Вычисление длины стороны:
function getSide($point1, $point2) {
	$result = 0;
	$x1 = $point1['x'];
	$y1 = $point1['y'];
	$x2 = $point2['x'];
	$y2 = $point2['y'];
	// Вычисляем сторону l:
	$l = sqrt((pow((abs(($x1 - $x2))), 2 ) + pow((abs(($y1 - $y2))), 2)));
	if ($l > 0) return $result = $l;
	else return $result = false;
}
