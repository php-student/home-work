function getTriangleArea($a, $b, $c) {
	// Решаем задачу, используя формулу Герона.
	// https://ru.wikipedia.org/wiki/Формула_Герона
	// Вычисляем стороны:
	$ab = getSide($a, $b);
	if ($ab == false) return $result = 'Длина стороны AB равна нулю.';
	$bc = getSide($b, $c);
	if ($bc == false) return $result = 'Длина стороны BC равна нулю.';
	$ac = getSide($a, $c);
	if ($ac == false) return $result = 'Длина стороны AC равна нулю.';

	// Вычисляем "p":
	$p = ($ab + $bc + $ac) / 2;

	// Вычисляем, наконец, площадь:
	$result = sqrt($p * ($p - $ab) * ($p - $bc) * ($p - $ac));
	return $result;
}
